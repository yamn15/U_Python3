print('******************************************************************')

# tupleの定義
num_tuple = (10, 20)
print(num_tuple)

print('******************************************************************')

# tupleのアンパッキング
## tupleの要素が、x ,yに入り、tupleが解除されている
x, y = num_tuple
print(num_tuple)

#上はx, yに、値を定義しているのと同じような意味になる。
x, y = (10, 20)
print(x, y)

# 丸括弧なくても大丈夫。Pythonではtupleを展開するという意味になる。
x, y = 10, 20
print(x, y)

print('******************************************************************')

#上はx, yに、値を定義しているのと同じような意味になる。
min, max = 0, 100
print(min, max)

