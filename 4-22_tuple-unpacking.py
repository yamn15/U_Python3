# アンパッキングを利用した数字の入れ替え
i = 10
j = 20

# iとjを入れ替えたいとき、jの20をiにいきなり入れると値が上書きされてしまう。
## 一時的な変数を用意し、入れ替える
### 入れ替えに3行もかかってしまう。
tmp = i
i = j
j = tmp
print(i, j)

# アンパッキングを利用した変数のかんたんな入れ替え
a = 100
b = 200
print(a, b)
a, b = b, a
print(a, b)


